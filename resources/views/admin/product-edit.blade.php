@push('scripts')
    <script>
        $(document).ready(function() {
            // توليد slug تلقائيًا عند إدخال name
            $("#name").on("input", function() {
                let name = $(this).val();
                let slug = name.toLowerCase().replace(/ /g, "-").replace(/[^\w-]+/g, "");
                $("#slug").val(slug);
            });

            // عرض الصورة المحددة قبل الرفع
            $("#myFile").on("change", function(event) {
                let file = event.target.files[0];
                if (file) {
                    let reader = new FileReader();
                    reader.onload = function(e) {
                        $("#previewImg").attr("src", e.target.result);
                        $("#imgpreview").show();
                    };
                    reader.readAsDataURL(file);
                }
            });
            $("#gFile").on("change", function(event) {
                let files = event.target.files;
                let previewContainer = $(".gimgpreview");

                previewContainer.empty().show(); // Clear previous images and show preview container

                if (files.length > 0) {
                    for (let i = 0; i < files.length; i++) {
                        let file = files[i];
                        let reader = new FileReader();

                        reader.onload = function(e) {
                            let imgElement = $("<img>").attr("src", e.target.result).css({
                                width: "100px",
                                height: "100px",
                                margin: "5px",
                                borderRadius: "5px",
                                objectFit: "cover"
                            });
                            previewContainer.append(imgElement);
                        };

                        reader.readAsDataURL(file);
                    }
                }
            });
        });
    </script>
@endpush
